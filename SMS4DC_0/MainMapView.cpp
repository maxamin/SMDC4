void CMainMapView::RectInRegionVec(double xlatO1 ,double xlatO2 ,double xlonO1 ,double xlonO2) 
{
	for (int i=0;i<286;i++)
	{
		if (((((lonlim1[i] >= xlonO1) && (lonlim1[i] <= xlonO2))  ||
			  ((lonlim2[i] >= xlonO1) && (lonlim2[i] <= xlonO2))) &&
			 (((latlim1[i] >= xlatO1) && (latlim1[i] <= xlatO2))  ||
			  ((latlim2[i] >= xlatO1) && (latlim2[i] <= xlatO2))))    ||
			((((xlonO1 >= lonlim1[i]) && (xlonO1 <= lonlim2[i]))  ||
			  ((xlonO2 >= lonlim1[i]) && (xlonO2 <= lonlim2[i]))) &&
			 (((xlatO1 >= latlim1[i]) && (xlatO1 <= latlim2[i]))  ||
			  ((xlatO2 >= latlim1[i]) && (xlatO2 <= latlim2[i])))))
			rgnIndex[i]=1;
		else
			rgnIndex[i]=0;
	}
}

void CMainMapView::DrawVectorN(CDC *pDC,int Vector_no,CRgn *rgn1,
							int nWidth,COLORREF crColor, int nStyle,
							BOOL nMode,COLORREF flColor,int alpha) 
{
	FILE *fp;
	CString IndFile_HI =  ((CSMS4DCApp *)AfxGetApp())->m_AppPath + _T("Texts\\IndFile_HI.dat");
	if ( (fp=fopen(IndFile_HI,"rb")) != NULL )
	{
		CString MainFile_HI =  ((CSMS4DCApp *)AfxGetApp())->m_AppPath + _T("Texts\\MainFile_HI.dat");
		FILE *fp2;
		if ( (fp2=fopen(MainFile_HI,"rb")) != NULL )
		{
			struct IFile IRec;
			struct MFile MRec;

			int k1=0,k2=0,i=0,a=1;
			fseek(fp,Vector_no*sizeof(IRec),SEEK_SET);
			fread(&IRec,sizeof(IRec),1,fp);
			double *datalon;	datalon=new double[IRec.Points];
			double *datalat;	datalat=new double[IRec.Points];
			DWORD *Nk;			Nk=new DWORD[10000];
			CPoint *pp;			pp=new CPoint[IRec.Points];
			fseek(fp2,IRec.Offset,SEEK_SET);
			for(int points=0;points<IRec.Points;points++)
			{
				fread(&MRec,sizeof(MRec),1,fp2);
				
				Nk[k1]=k2;
				if (MRec.Lat==-200)
				{
					k1++;a=0;
				}
				else
				{
					datalon[i]=MRec.Lon;
					datalat[i]=MRec.Lat;
					LatLon2Point(datalat[i],datalon[i],&pp[i]);
					i++;k2++;a=1;
				}
				k2=a*k2;
			}
			fclose(fp2);
			fclose(fp);

			CPen pen(nStyle, nWidth, crColor);
			CPen* pOldPen = pDC->SelectObject(&pen);
			CBrush brush(flColor);
			CBrush* pOldBrush = pDC->SelectObject(&brush);

			rgn1->DeleteObject();
			rgn1->CreatePolyPolygonRgn(pp,(int*)Nk,k1+1,WINDING);

			if (nMode)
				pDC->PolyPolygon(pp,(int*)Nk,k1+1);

			pDC->PolyPolyline(pp,Nk,k1+1);

			pDC->SelectObject(pOldPen);
			pDC->SelectObject(pOldBrush);
			delete [] datalon;
			delete [] datalat;
			delete [] Nk;
			delete [] pp;
		}
	}
}


void CMainMapView::LatLon2Point(double lat_deg,double lon_deg,CPoint *PointSt) 
{
	double lon1 = lonO1  ,  lon2 = lonO2;
	double lat1 = latO1  ,  lat2 = latO2;

	double x_en,y_en;

	double aX = AxesW / (lon2-lon1);
	double bX = AxesL - AxesW*lon1/(lon2-lon1);
	x_en = aX * lon_deg + bX ;

	double aY = AxesH / (lat1-lat2);
	double bY = AxesT - AxesH*lat2/(lat1-lat2);
	y_en = aY * lat_deg + bY ;

	(*PointSt).x = (long)x_en;
	(*PointSt).y = (long)y_en;
}

void CMainMapView::Point2LatLon(CPoint PointSt,double *lat_deg,double *lon_deg) 
{
	double lon1 = lonO1  ,  lon2 = lonO2;
	double lat1 = latO1  ,  lat2 = latO2;

	double aX = AxesW / (lon2-lon1);
	double bX = AxesL - AxesW*lon1/(lon2-lon1);
	*lon_deg = (PointSt.x - bX) / aX;


	double aY = AxesH / (lat1-lat2);
	double bY = AxesT - AxesH*lat2/(lat1-lat2);
	*lat_deg = (PointSt.y - bY) / aY;
}

